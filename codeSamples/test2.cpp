#include <iostream>
using namespace std;

int main()
{
  // this reserves 10 integer elements
  int array[10]; 
  int i;

  // load the array
  for(i = 0; i < 10; i++)
    array[i] = i;

  // display the array
  for(i = 0; i < 10; ++i)
    {
      cout << array[i] ;
      cout << endl;
    }
  return 0;
}
